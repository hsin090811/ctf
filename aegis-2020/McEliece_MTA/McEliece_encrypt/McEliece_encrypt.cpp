int bitcount(unsigned int noise)
{
	int count = 0;
	while (noise)
	{
		count++;
		noise &= (noise - 1);
	}
	return count;
}

void bintochar(int* bit, int len_b, unsigned char* message)
{
	int len_m = len_b / 8;
	for (int i = 0; i < len_m; i++)
	{
		for (int j = 0; j < 8; j++)
		{
			message[i] = message[i] ^ (bit[8 * i + j] << (7 - j));
		}
	}
}


